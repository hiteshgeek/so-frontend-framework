    <script>
        // Mobile menu toggle
        const mobileMenuToggle = document.getElementById('mobileMenuToggle');
        const mobileMenu = document.getElementById('mobileMenu');
        const menuIcon = document.getElementById('menuIcon');

        if (mobileMenuToggle && mobileMenu) {
            mobileMenuToggle.addEventListener('click', () => {
                const isHidden = mobileMenu.classList.contains('so-d-none');
                mobileMenu.classList.toggle('so-d-none', !isHidden);
                mobileMenu.classList.toggle('so-d-block', isHidden);
                if (menuIcon) menuIcon.textContent = isHidden ? 'close' : 'menu';
            });
        }

        // Theme toggle
        const themeToggle = document.getElementById('themeToggle');
        const themeToggleMobile = document.getElementById('themeToggleMobile');
        const themeIcon = document.getElementById('themeIcon');

        function toggleTheme() {
            const html = document.documentElement;
            const currentTheme = html.getAttribute('data-theme');
            const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
            html.setAttribute('data-theme', newTheme);

            // Update icons
            const iconName = newTheme === 'dark' ? 'light_mode' : 'dark_mode';
            if (themeIcon) themeIcon.textContent = iconName;

            // Update body background
            document.body.classList.toggle('so-bg-light', newTheme === 'light');
            document.body.classList.toggle('so-bg-dark', newTheme === 'dark');

            // Save preference
            localStorage.setItem('theme', newTheme);
        }

        if (themeToggle) themeToggle.addEventListener('click', toggleTheme);
        if (themeToggleMobile) themeToggleMobile.addEventListener('click', toggleTheme);

        // Load saved theme
        const savedTheme = localStorage.getItem('theme');
        if (savedTheme) {
            document.documentElement.setAttribute('data-theme', savedTheme);
            if (themeIcon) themeIcon.textContent = savedTheme === 'dark' ? 'light_mode' : 'dark_mode';
            document.body.classList.toggle('so-bg-light', savedTheme === 'light');
            document.body.classList.toggle('so-bg-dark', savedTheme === 'dark');
        }
    </script>

